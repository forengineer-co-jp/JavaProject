package Homework_07_02;

import java.util.Scanner;

public class Pound {
    public static void main(String[] args)
    {
        /* オブジェクト生成 */
        Scanner input = new Scanner(System.in);

        System.out.println("最大重量(kg)を入力:");
        int n = input.nextInt();

        /* 計算式 */
        final double p_per_kg = 2.2;

        /* 結果を出力 */
        System.out.println("kg        p");
        for (int i = 1; i <= n; i++)
        {
            System.out.printf("%d%10.1f\n", i, (i * p_per_kg));
        }
    }
}

