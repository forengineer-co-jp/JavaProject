package Homework_07_04;

import java.util.Scanner;

public class Kg_And_Ft
{
    public static void main(String[] args)
    {
        Scanner input = new Scanner(System.in);

        /* 数字を入力 */
        System.out.println("数字を入力:");
        int n = input.nextInt();

        /* 計算式 */
        final double p_per_kg = 2.2;

        /* 結果を出力 */
        System.out.println("Kg              P       |     P                Kg");
        for (int i = 1, j = 1; i <= n && j <= n;  i++, j++)
        {
            System.out.printf("%-12d%7.1f", i, (i * p_per_kg));
            System.out.print("     |     ");
            System.out.printf("%-9d%12.2f\n", j, (j / p_per_kg));
        }
    }
}

