package Homework_07_03;

import java.util.Scanner;

public class Kilometers {

    public static void main(String[] args)
    {

        /* オブジェクト生成 */
        Scanner input = new Scanner(System.in);

        System.out.println("最大英尺(ft)を入力:");
        int n = input.nextInt();

        /* 計算式 */
        final double km_per_ft = 1.609;

        /* 結果を出力 */
        System.out.println("ft          km");
        for (int i = 1; i <= n; i++)
        {
            System.out.printf("%d%16.3f\n", i, (i * km_per_ft));
        }
    }
}

